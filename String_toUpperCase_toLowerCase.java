package day01;

public class String_toUpperCase_toLowerCase {
	/**
	 * String toUpperCase()
	 * String toLowerCase()
	 * 将当前字符串中的英文部分转换为全大写或者全小写
	 * */
	public static void main(String[] args) {
		String str = "java123";
		System.out.println(str.toUpperCase());
		System.out.println(str.toLowerCase()/*.toUpperCase()*/);
	}
}
